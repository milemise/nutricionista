private void buscarAlimento() {
    String textoBusqueda = jTextFieldBuscar.getText().toLowerCase().trim(); // Convertir a min√∫sculas y eliminar espacios.
    DefaultTableModel modelo = (DefaultTableModel) jTableResultados.getModel();
    modelo.setRowCount(0); // Limpia los resultados previos.

     String sql = "SELECT * FROM alimento WHERE LOWER(nombre) LIKE ?";
        String DATABASE_URL = null;
    try (Connection conn = Conexion.getConexion(DATABASE_URL); 
         PreparedStatement ps = conn.prepareStatement(sql)) {
        
        ps.setString(1, "%" + textoBusqueda + "%");
        ResultSet rs = ps.executeQuery();

        System.out.println("Ejecutando consulta con texto: " + textoBusqueda);

        boolean hayResultados = false;
        while (rs.next()) {
            hayResultados = true;
            modelo.addRow(new Object[]{
                rs.getInt("codComida"),
                rs.getString("nombre"),
                rs.getString("tipoComida"),
                rs.getDouble("caloriasPor100g"),
                rs.getString("detalle"),
                rs.getInt("baja"),
                rs.getInt("aptoVegetariano"),
                rs.getInt("libredeTACC"),
                rs.getInt("lacteo")
            });
        }

        if (!hayResultados) {
            JOptionPane.showMessageDialog(this, "No se encontraron resultados para: " + textoBusqueda);
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error al buscar alimentos: " + e.getMessage());
        e.printStackTrace();
    }
}




